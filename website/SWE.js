function locationURL(page) {
     if (page === "Projects") {
         location.href = "PP.html";
     } else if (page === "Experience") {
         location.href = "elp.html";
     } else if (page === "Education") {
         location.href = "ee.html";
     } else if (page === "Contact") {
         location.href = "cc.html";
     }
}
 
 




